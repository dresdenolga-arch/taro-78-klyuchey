  function guessSituationCategory(text) {
    const n = norm(text || "");
    if (!n.trim()) return null;
    let best = null, bestHits = 0;
    SITUATION_CATEGORIES.forEach((cat) => {
      const hits = cat.keywords.filter((k) => n.includes(norm(k))).length;
      if (hits > bestHits) { bestHits = hits; best = cat; }
    });
    return best;
  }

